class Solution
{
    public:
    //Function to count number of ways to reach the nth stair.
    int countWays(int n)
    {
        int M = 1e9+7;
        int a = 1; int b = 1;
        for(int i=0; i<n-1; i++){
            int c = (a%M+b%M)%M;
            a = b;
            b = c;
        }
        return b;
    }
};



